package CentralPoint;

public class ChatCloseInfo {
    private boolean isChatClose;

    public ChatCloseInfo(boolean isChatClose) {
        this.isChatClose = isChatClose;
    }
    
    public void setIsChatClose(boolean isChatClose) {
        this.isChatClose = isChatClose;
    }
    
    public boolean getIsChatClose() {
        return this.isChatClose;
    }
    
}
